void solve(){
    ll a, b, c;
    cin >> a >> b >> c;
    vector < tuple< ll, ll , ll > > all;
    for(int i = 0; i <= 5; i++){
        for(int j = 0; j <= 5; j++){
            if(i + j <= 5){
                int k = 5 - i - j;
                all.push_back({i, j, k});
            }
        }
    }
    ll ans = a * b * c;
    for(auto x : all){
        ll aa = a + (get<0>(x));
        ll bb = b + (get<1>(x));
        ll cc = c + (get<2>(x));
        ans = max(ans, aa * bb * cc);
    }
    cout << ans << endl;
}


